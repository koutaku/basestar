package org.typeunsafe;


public class Constraints {
  public Double border;
  public Double width;
  public Double height;
  public Double maxVelocity;

  public Constraints(Double border, Double width, Double height, Double maxVelocity) {
    this.border = border;
    this.width = width;
    this.height = height;
    this.maxVelocity = maxVelocity;
  }
}
